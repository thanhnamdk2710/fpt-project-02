    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);

        $user->status = !$user->status;
        $user->save();

        return redirect(route('customers.index'))->with('success', 'Update Status Successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);

        if(file_exists('images/users/' . $user->profile->avatar)){
            unlink('images/users/' . $user->profile->avatar);
        }

        $user->profile()->delete();
        $user->delete();

        return redirect(route('customers.index'))->with('success', 'Delete User Successfully');
    }
